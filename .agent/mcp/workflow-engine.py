# ── Safe file writing ──

def _safe_write_json(path, data):
    """Write JSON atomically: temp file + rename, with fallback to direct write."""
    content = json.dumps(data, indent=2, ensure_ascii=False) + "\n"
    tmp_path = path + ".tmp"
    try:
        with open(tmp_path, "w", encoding="utf-8") as f:
            f.write(content)
        os.replace(tmp_path, path)
    except OSError:
        try:
            # Fallback: direct write (e.g., os.replace fails on Windows if target is locked)
            with open(path, "w", encoding="utf-8") as f:
                f.write(content)
        except OSError as e2:
            _log(f"Warning: failed to write {path}: {e2}")


def _safe_write_text(path, content):
    """Write text file atomically: temp file + rename, with fallback."""
    os.makedirs(os.path.dirname(path), exist_ok=True)
    tmp_path = path + ".tmp"
    try:
        with open(tmp_path, "w", encoding="utf-8") as f:
            f.write(content)
        os.replace(tmp_path, path)
    except OSError:
        try:
            with open(path, "w", encoding="utf-8") as f:
                f.write(content)
        except OSError as e2:
            _log(f"Warning: failed to write {path}: {e2}")


# ── YAML Loading ──

_yaml_module = None

def _get_yaml():
    """Lazy-load PyYAML (same dependency as gate-check.py)."""
    global _yaml_module
    if _yaml_module is None:
        try:
            import yaml
            _yaml_module = yaml
        except ImportError:
            raise RuntimeError(
                "PyYAML is required: pip install pyyaml "
                "(same dependency as gate-check.py)"
            )
    return _yaml_module


def load_yaml(path):
    """Load a YAML file and return parsed data."""
    yaml = _get_yaml()
    with open(path, "r", encoding="utf-8") as f:
        return yaml.safe_load(f)


