# MCP Protocol: JSON-RPC 2.0 over stdio
# ═══════════════════════════════════════════════════════════════════════════

def _make_response(msg_id, result):
    """Build a JSON-RPC 2.0 success response."""
    return {"jsonrpc": "2.0", "id": msg_id, "result": result}


def _make_error(msg_id, code, message):
    """Build a JSON-RPC 2.0 error response."""
    return {"jsonrpc": "2.0", "id": msg_id, "error": {"code": code, "message": message}}


def handle_message(msg):
    """Process a single JSON-RPC message. Returns response dict or None."""
    method = msg.get("method", "")
    msg_id = msg.get("id")
    params = msg.get("params", {})

    # ── Initialize handshake ──
    if method == "initialize":
        return _make_response(msg_id, {
            "protocolVersion": "2024-11-05",
            "capabilities": {"tools": {}},
            "serverInfo": {
                "name": "workflow-engine",
                "version": "1.0.0",
            },
        })

    # ── Notifications (no response) ──
    if method == "notifications/initialized":
        return None

    # ── List tools ──
    if method == "tools/list":
        return _make_response(msg_id, {"tools": TOOLS})

    # ── Call tool ──
    if method == "tools/call":
        tool_name = params.get("name", "")
        arguments = params.get("arguments", {})

        handler = TOOL_HANDLERS.get(tool_name)
        if not handler:
            return _make_response(msg_id, {
                "content": [{"type": "text", "text": f"Unknown tool: {tool_name}"}],
                "isError": True,
            })

        call_start = datetime.now(timezone.utc).astimezone()
        try:
            result = handler(arguments)
            call_end = datetime.now(timezone.utc).astimezone()
            duration_ms = int((call_end - call_start).total_seconds() * 1000)

            # Log the MCP call
            entry = {
                "timestamp": call_start.isoformat(),
                "tool": tool_name,
                "duration_ms": duration_ms,
                "success": True,
            }
            # Extract contextual fields
            wf_name = arguments.get("name") or arguments.get("workflow_name")
            step = arguments.get("step") or arguments.get("step_name")
            if wf_name:
                entry["workflow"] = wf_name
            if step:
                entry["step"] = step
            if isinstance(result, dict):
                summary = _summarize_call(tool_name, arguments, result)
                if summary:
                    entry["summary"] = summary
            _mcp_call_log.append(entry)

            # Handler returns dict — serialize to JSON text
            if isinstance(result, dict):
                text = json.dumps(result, indent=2, ensure_ascii=False)
            else:
                text = str(result)
            return _make_response(msg_id, {
                "content": [{"type": "text", "text": text}],
            })
        except Exception as e:
            call_end = datetime.now(timezone.utc).astimezone()
            duration_ms = int((call_end - call_start).total_seconds() * 1000)

            # Log failed call
            entry = {
                "timestamp": call_start.isoformat(),
                "tool": tool_name,
                "duration_ms": duration_ms,
                "success": False,
                "error": f"{type(e).__name__}: {e}",
            }
            wf_name = arguments.get("name") or arguments.get("workflow_name")
            step = arguments.get("step") or arguments.get("step_name")
            if wf_name:
                entry["workflow"] = wf_name
            if step:
                entry["step"] = step
            _mcp_call_log.append(entry)

            _log(f"Tool '{tool_name}' error: {type(e).__name__}: {e}")
            return _make_response(msg_id, {
                "content": [
                    {"type": "text", "text": f"Error: {type(e).__name__}: {e}"}
                ],
                "isError": True,
            })

    # ── Unknown method ──
    if msg_id is not None:
        return _make_error(msg_id, -32601, f"Unknown method: {method}")

    return None  # Unknown notification — ignore


def main():
    """Main loop: read newline-delimited JSON-RPC from stdin, respond on stdout."""
    # Force UTF-8 on Windows where stdin/stdout default to system encoding (e.g., CP1251)
    if hasattr(sys.stdin, "reconfigure"):
        sys.stdin.reconfigure(encoding="utf-8", errors="replace")
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8")
    if hasattr(sys.stderr, "reconfigure"):
        sys.stderr.reconfigure(encoding="utf-8")

    _log("Starting workflow-engine MCP server")

    for line in sys.stdin:
        line = line.strip()
        if not line:
            continue

        try:
            msg = json.loads(line)
        except json.JSONDecodeError as e:
            _log(f"Invalid JSON input: {e}")
            continue

        response = handle_message(msg)
        if response is not None:
            sys.stdout.write(json.dumps(response, ensure_ascii=False) + "\n")
            sys.stdout.flush()


if __name__ == "__main__":
    main()
