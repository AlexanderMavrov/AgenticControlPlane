#!/usr/bin/env python3
"""
workflow-engine.py — MCP server for the Workflow Engine.

Provides tools for workflow orchestration: resolve, init, resume,
step management, and finalization. Replaces manual file operations
in the orchestrator's SKILL.md with efficient tool calls.

Transport: stdio (JSON-RPC 2.0, newline-delimited)
Dependencies: PyYAML (same as gate-check.py)

Tools:
    workflow_resolve       — Find workflow definition, return metadata
    workflow_init          — Create manifest + trace, generate run_token,
                             check tool availability
    workflow_resume        — Resume from existing manifest
    step_begin             — Prepare step: pre-compose text blocks for subagent task,
                             return per-step subagent_type for the orchestrator
                             to spawn (CC) or enriched goal docs (Cursor)
    step_collect_result    — Read gate-result.json, determine next action
    step_complete          — Mark step done, write summary, apply param_bindings
    workflow_finalize      — Complete workflow, finalize trace with aggregates

Architecture (Phase 38):
    Script tools are exposed by a separate, stable workflow-tools-loader.py
    MCP server registered in .mcp.json. Per-step subagent files (one per
    workflow step with subagent: true) are generated by init-workflow.py
    and live in .claude/agents/<workflow>-<step>/AGENT.md, each with a
    strict tools whitelist. This engine never modifies AGENT.md at runtime.
"""

