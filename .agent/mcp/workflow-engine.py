def _summarize_call(tool_name, arguments, result):
    """Produce a 1-line human-readable summary of an MCP tool call."""
    if not isinstance(result, dict):
        return ""
    try:
        if tool_name == "workflow_resolve":
            return (
                f"Resolved {result.get('name', '?')} "
                f"v{result.get('version', '?')}, "
                f"{result.get('step_count', '?')} steps"
            )
        if tool_name == "workflow_init":
            return f"Initialized run {result.get('run_id', '?')}"
        if tool_name == "workflow_resume":
            return (
                f"Resumed {arguments.get('name', '?')}, "
                f"step: {result.get('current_step', '?')}"
            )
        if tool_name == "step_begin":
            stype = result.get("type", "regular")
            step = result.get("step_name", arguments.get("step", "?"))
            if stype == "delegation":
                return f"Delegation {step} → {result.get('delegate_to', '?')}"
            sub = result.get("subagent", True)
            return f"Prepared {step} ({stype}, subagent: {sub})"
        if tool_name == "step_collect_result":
            passed = result.get("passed")
            step = result.get("step", arguments.get("step", "?"))
            action = result.get("action", "?")
            if passed is True:
                return f"Gate passed for {step} → {action}"
            if passed is False:
                return f"Gate failed for {step} → {action}"
            return f"{action} for {step}"
        if tool_name == "step_complete":
            step = result.get("step", arguments.get("step", "?"))
            nxt = result.get("next_step")
            done = result.get("workflow_done", False)
            if done:
                return f"Completed {step} (workflow done)"
            return f"Completed {step}, next: {nxt}"
        if tool_name == "workflow_finalize":
            return (
                f"Finalized {result.get('workflow', '?')}, "
                f"status: {result.get('status', '?')}"
            )
        if tool_name == "list_agent_files":
            return f"Listed {result.get('count', '?')} files"
    except Exception:
        pass
    return ""


# ── Workflow Resolution ──

def resolve_workflow(name):
    """Find workflow.yaml by name. Returns (definition_dir, workflow_data).

    Search order:
      1. .agent/workflows/templates/my_workflows/<name>/workflow.yaml
      2. .agent/workflows/templates/predefined/<name>/workflow.yaml
      3. .agent/workflows/<name>/workflow.yaml  (legacy)

    Raises FileNotFoundError if not found.
    """
    if name in _workflow_cache:
        cached_dir, cached_data, cached_mtime = _workflow_cache[name]
        # Check if file changed since caching
        cached_path = os.path.join(cached_dir, "workflow.yaml")
        try:
            current_mtime = os.path.getmtime(cached_path)
            if current_mtime == cached_mtime:
                return cached_dir, cached_data
        except OSError:
            pass
        # Stale — fall through to re-read

    search_paths = [
        os.path.join(MY_WORKFLOWS_DIR, name, "workflow.yaml"),
        os.path.join(PREDEFINED_DIR, name, "workflow.yaml"),
        os.path.join(EXAMPLES_DIR, name, "workflow.yaml"),
        os.path.join(WORKFLOWS_DIR, name, "workflow.yaml"),
    ]

    for path in search_paths:
        if os.path.isfile(path):
            defn_dir = os.path.dirname(path)
            wf_data = load_yaml(path)
            mtime = os.path.getmtime(path)
            _workflow_cache[name] = (defn_dir, wf_data, mtime)
            return defn_dir, wf_data

    raise FileNotFoundError(
        f"Workflow '{name}' not found. "
        f"Searched: {', '.join(search_paths)}"
    )


# ── Helper: resolve {param} placeholders ──

def _resolve_placeholders(text, params):
    """Replace {param} placeholders in text with actual param values."""
    if not text or not params:
        return text
    result = text
    for pname, pval in params.items():
        placeholder = f"{{{pname}}}"
        replacement = str(pval) if pval is not None else ""
        result = result.replace(placeholder, replacement)
    return result


# ── Helper: read JSON field by dot-notation path ──

def _read_json_field(data, field_path):
    """Navigate a JSON object using dot notation (e.g., 'metadata.name')."""
    value = data
    for key in field_path.split("."):
        if isinstance(value, dict):
            value = value[key]
        else:
            raise KeyError(f"Cannot navigate '{key}' in non-dict: {type(value)}")
    return value


def _resolve_parallel_branches(parallel_key, step_config, runtime_dir):
    """Resolve parallel_key to a list of branch items.

    parallel_key format: "struct-name.field.path[filter_key=filter_value]"
    Example: "analysis-report.domains[status=pending]"

    Returns list of dicts (the matching items from the data file).
    Raises ValueError if resolution fails.
    """
    m = re.match(r'^([^.]+)\.(.+?)(?:\[(\w+)=(\w+)\])?$', parallel_key)
    if not m:
        raise ValueError(f"Cannot parse parallel_key: {parallel_key}")

    struct_name = m.group(1)
    field_path = m.group(2)
    filter_key = m.group(3)
    filter_val = m.group(4)

    # Find the data file: map struct name to input file path
    # Convention: struct "analysis-report" → data/analysis-report.json
    data_file = os.path.join(runtime_dir, "data", f"{struct_name}.json")
    if not os.path.isfile(data_file):
        raise ValueError(
            f"Parallel key references struct '{struct_name}' but "
            f"data file not found: {data_file}"
        )

    with open(data_file, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Navigate to field
    items = data
    for key in field_path.split("."):
        if isinstance(items, dict) and key in items:
            items = items[key]
        else:
            raise ValueError(
                f"Cannot navigate '{key}' in parallel_key '{parallel_key}'"
            )

    if not isinstance(items, list):
        raise ValueError(
            f"parallel_key '{parallel_key}' resolved to {type(items).__name__}, "
            f"expected list"
        )

    # Apply filter if present
    if filter_key and filter_val:
        items = [it for it in items if str(it.get(filter_key)) == filter_val]

    return items


# ── Workflow tools availability check ──
#
# Phase 38 architecture: script tools are now exposed by the separate
# workflow-tools-loader.py MCP server (registered in .mcp.json), and
# per-step subagent files with strict tools whitelists are generated
# by .agent/scripts/init-workflow.py. This engine no longer wraps
# script tools as a per-workflow MCP proxy and no longer modifies
# .claude/agents/workflow-step/AGENT.md at runtime — both approaches
# were proven empirically broken in Claude Code (see discussion.md
# entries 2026-04-06 and 2026-04-07).


def _check_tools_availability(tools_config):
    """Check availability of declared workflow tools.

    Returns dict with 'available' and 'missing' lists.
    Script tools: checks if command base executable seems valid.
    MCP tools: checks if server_config is provided (actual MCP check
    requires the orchestrator to verify via tools/list).
    """
    available = []
    missing = []

    for tool in tools_config:
        tool_name = tool.get("name", "unknown")
        tool_type = tool.get("type", "script")

        if tool_type == "script":
            command = tool.get("command", "")
            # Extract the base command (first word)
            parts = command.split()
            if not parts:
                missing.append({
                    "name": tool_name,
                    "type": "script",
                    "required": tool.get("required", False),
                    "reason": "Empty command",
                })
                continue

            base_cmd = parts[0]
            # Check if it's a python script with a path
            if len(parts) > 1 and base_cmd in ("python", "python3", sys.executable):
                script_path = parts[1]
                if os.path.isfile(script_path):
                    available.append({"name": tool_name, "type": "script"})
                else:
                    missing.append({
                        "name": tool_name,
                        "type": "script",
                        "required": tool.get("required", False),
                        "reason": f"Script not found: {script_path}",
                    })
            else:
                # For non-python commands, assume available (hard to check on Windows)
                available.append({"name": tool_name, "type": "script"})

        elif tool_type == "mcp":
            # MCP server dependency: we can only check if server_config exists
            # Actual runtime check must be done by the orchestrator
            has_config = bool(tool.get("server_config"))
            entry = {
                "name": tool_name,
                "type": "mcp",
                "required": tool.get("required", True),
                "has_server_config": has_config,
            }
            if has_config:
                entry["server_config"] = tool["server_config"]
            # MCP servers are always "needs_verification" — orchestrator must check
            entry["needs_verification"] = True
            available.append(entry)

    return {"available": available, "missing": missing}


