def tool_workflow_resolve(arguments):
    """Find and return workflow definition metadata."""
    name = arguments["name"]
    defn_dir, wf = resolve_workflow(name)

    steps_info = []
    for s in wf.get("steps", []):
        info = {"name": s["name"]}
        if "delegate_to" in s:
            info["delegate_to"] = s["delegate_to"]
        if s.get("parallel"):
            info["parallel"] = True
        if s.get("gate"):
            info["gate_override"] = s["gate"]
        if s.get("model"):
            info["model"] = s["model"]
        steps_info.append(info)

    # Tools metadata
    tools_config = wf.get("tools", [])
    tools_info = []
    for t in tools_config:
        ti = {"name": t.get("name"), "type": t.get("type", "script")}
        if t.get("description"):
            ti["description"] = t["description"]
        if t.get("required") is not None:
            ti["required"] = t["required"]
        tools_info.append(ti)

    result = {
        "name": wf.get("name", name),
        "version": wf.get("version", 1),
        "description": wf.get("description", ""),
        "definition_dir": defn_dir,
        "params": wf.get("params", []),
        "gate": wf.get("gate", {}),
        "context": wf.get("context", {}),
        "steps": steps_info,
        "step_count": len(steps_info),
    }
    if tools_info:
        result["tools"] = tools_info
    return result


def tool_workflow_init(arguments):
    """Create a new workflow run: manifest, trace, run_token."""
    name = arguments.get("name") or arguments.get("workflow_name")
    user_params = arguments.get("params", {})
    # LLM sometimes passes params as JSON string instead of object
    if isinstance(user_params, str):
        try:
            user_params = json.loads(user_params)
        except (json.JSONDecodeError, ValueError):
            user_params = {}

    defn_dir, wf = resolve_workflow(name)

    # ── Concurrent run guard ──
    # Only one run per workflow at a time. If manifest exists with
    # status "in_progress", reject to prevent state corruption.
    runtime_dir = os.path.join(WORKFLOWS_DIR, name)
    existing_manifest = os.path.join(runtime_dir, "manifest.json")
    if os.path.isfile(existing_manifest):
        try:
            with open(existing_manifest, "r", encoding="utf-8") as f:
                prev = json.load(f)
            if prev.get("status") == "in_progress":
                return {
                    "error": (
                        f"Workflow '{name}' already has an in-progress run "
                        f"(run_id: {prev.get('run_id')}). Wait for it to "
                        f"complete or finalize it before starting a new one."
                    ),
                    "active_run_id": prev.get("run_id"),
                }
        except (json.JSONDecodeError, OSError):
            pass  # Corrupted manifest — safe to overwrite

    # ── Resolve params ──
    param_defs = wf.get("params", [])
    resolved_params = {}

    for pdef in param_defs:
        pname = pdef["name"]
        if pname in user_params and user_params[pname] is not None:
            resolved_params[pname] = user_params[pname]
        elif "default" in pdef:
            resolved_params[pname] = pdef["default"]
        elif pdef.get("required"):
            return {"error": f"Required param '{pname}' not provided"}
        else:
            resolved_params[pname] = None

    # Include extra params not in definitions (pass-through)
    for k, v in user_params.items():
        if k not in resolved_params:
            resolved_params[k] = v

    # ── Generate identifiers ──
    now = datetime.now(timezone.utc).astimezone()
    now_iso = now.isoformat()
    run_id = f"{name}-{now.strftime('%Y%m%d-%H%M%S')}-{uuid.uuid4().hex[:4]}"
    run_token = str(uuid.uuid4())

    # Inject run_id as a synthetic param so {run_id} works in output paths
    resolved_params["run_id"] = run_id

    # ── Build manifest ──
    step_names = [s["name"] for s in wf.get("steps", [])]
    steps_dict = {sname: {"status": "pending"} for sname in step_names}

    manifest = {
        "workflow": name,
        "workflow_version": wf.get("version", 1),
        "run_id": run_id,
        "run_token": run_token,
        "status": "in_progress",
        "current_step": step_names[0] if step_names else None,
        "started_at": now_iso,
        "updated_at": now_iso,
        "params": resolved_params,
        "steps": steps_dict,
    }

    # ── Clean stale data from previous runs ──
    # New run (not resume): clear data/ and context/ so subagents don't
    # pick up stale outputs from a previous run. Trace files and manifest
    # are overwritten anyway; data/ and context/ are not.
    for stale_dir in ("data", "context"):
        stale_path = os.path.join(runtime_dir, stale_dir)
        if os.path.isdir(stale_path):
            shutil.rmtree(stale_path, ignore_errors=True)
            _log(f"Cleaned stale {stale_dir}/ from previous run")

    # ── Write manifest ──
    os.makedirs(runtime_dir, exist_ok=True)
    manifest_path = os.path.join(runtime_dir, "manifest.json")
    _safe_write_json(manifest_path, manifest)

    # ── Write trace ──
    wf_gate = wf.get("gate", {})
    trace = {
        "trace_version": 1,
        "workflow": name,
        "workflow_version": wf.get("version", 1),
        "run_id": run_id,
        "params": resolved_params,
        "gate_config": {
            "structural": wf_gate.get("structural", True),
            "semantic": wf_gate.get("semantic", False),
            "human": wf_gate.get("human", False),
            "max_gate_retries": wf_gate.get("max_gate_retries", 5),
            "max_step_retries": wf_gate.get("max_step_retries", 3),
        },
        "started_at": now_iso,
        "completed_at": None,
        "status": "in_progress",
        "total_duration_ms": None,
        "total_messages": 0,
        "total_tool_calls": 0,
        "total_modified_files": 0,
        "steps": [],
    }

    # Optional trace metadata fields
    if wf.get("description"):
        trace["description"] = wf["description"]
    if wf.get("context"):
        trace["context"] = wf["context"]
    if param_defs:
        trace["param_defs"] = [
            {
                "name": p["name"],
                "description": p.get("description", ""),
                "required": p.get("required", False),
            }
            for p in param_defs
        ]

    trace_dir = os.path.join(runtime_dir, "trace")
    os.makedirs(trace_dir, exist_ok=True)
    trace_path = os.path.join(trace_dir, f"{run_id}.trace.json")
    _safe_write_json(trace_path, trace)

    # ── Tools: availability check only ──
    # Phase 38: script tools come from workflow-tools-loader.py (registered
    # in .mcp.json), per-step subagents are pre-generated by init-workflow.py.
    # Engine no longer generates per-workflow proxies or modifies AGENT.md.
    tools_config = wf.get("tools", [])
    tools_result = {}
    if tools_config:
        tools_check = _check_tools_availability(tools_config)
        tools_result["available"] = tools_check["available"]
        tools_result["missing"] = tools_check["missing"]

        # Block if required tools are missing
        required_missing = [
            m for m in tools_check["missing"] if m.get("required")
        ]
        if required_missing:
            tools_result["blocked"] = True
            tools_result["block_message"] = (
                "Required tools are missing. Enable them before continuing:\n"
                + "\n".join(
                    f"  - {m['name']} ({m['type']}): {m.get('reason', 'not available')}"
                    for m in required_missing
                )
            )

    result = {
        "run_id": run_id,
        "run_token": run_token,
        "runtime_dir": runtime_dir,
        "first_step": step_names[0] if step_names else None,
        "total_steps": len(step_names),
        "step_names": step_names,
        "params": resolved_params,
    }
    if tools_result:
        result["tools"] = tools_result
    return result


def tool_workflow_resume(arguments):
    """Resume an existing workflow run from its manifest."""
    name = arguments["name"]
    runtime_dir = os.path.join(WORKFLOWS_DIR, name)
    manifest_path = os.path.join(runtime_dir, "manifest.json")

    if not os.path.isfile(manifest_path):
        return {
            "error": f"No manifest found for '{name}'. Start a new run without --resume."
        }

    with open(manifest_path, "r", encoding="utf-8") as f:
        manifest = json.load(f)

    current_step = manifest.get("current_step")
    step_data = manifest.get("steps", {}).get(current_step, {})

    # Collect summaries from completed steps
    completed_summaries = {}
    for sname, sdata in manifest.get("steps", {}).items():
        if sdata.get("status") == "completed":
            summary_path = os.path.join(
                runtime_dir, "context", f"{sname}.summary.md"
            )
            if os.path.isfile(summary_path):
                with open(summary_path, "r", encoding="utf-8") as f:
                    completed_summaries[sname] = f.read()

    wf_status = manifest.get("status")
    result = {
        "run_id": manifest.get("run_id"),
        "run_token": manifest.get("run_token"),
        "runtime_dir": runtime_dir,
        "status": wf_status,
        "current_step": current_step,
        "current_step_status": step_data.get("status"),
        "params": manifest.get("params", {}),
        "completed_summaries": completed_summaries,
        "steps": manifest.get("steps", {}),
    }
    if wf_status == "completed":
        result["warning"] = (
            "Workflow is already completed. Starting a new run "
            "without --resume may be more appropriate."
        )

    return result


# ── Tool docs rendering for step prompt enrichment ──

def _normalize_for_mcp(name):
    """Convert hyphens/non-word chars to underscores for MCP tool name compatibility."""
    return re.sub(r"[^a-zA-Z0-9_]", "_", name or "")


def _render_tool_docs_brief(active_tools, workflow_name):
    """Render a short affordance reminder when CC subagent already sees tools.

    The per-step subagent file already contains the full whitelist, so the
    LLM has the tool definitions in its inventory. This block just nudges
    the LLM toward the right tool by name and intent.
    """
    if not active_tools:
        return ""
    lines = ["**Available tools for this step:**"]
    for t in active_tools:
        name = t.get("name", "")
        desc = t.get("description", "")
        ttype = t.get("type", "script")
        if ttype == "script":
            wf_norm = _normalize_for_mcp(workflow_name)
            tool_norm = _normalize_for_mcp(name)
            lines.append(
                f"- `mcp__workflow_tools__{wf_norm}__{tool_norm}` "
                f"({name}) — {desc}"
            )
        else:
            expected = t.get("expected_tools") or []
            if expected:
                names = ", ".join(
                    f"`mcp__{_normalize_for_mcp(name)}__{_normalize_for_mcp(et)}`"
                    for et in expected
                )
                lines.append(f"- {name} ({ttype}): {names} — {desc}")
            else:
                lines.append(f"- {name} ({ttype}) — {desc}")
    lines.append(
        "Prefer these MCP tools over running the underlying scripts via Bash — "
        "they provide structured tracing and schema validation."
    )
    return "\n".join(lines)


def _render_tool_docs_full(active_tools, workflow_name):
    """Render full inline tool documentation for adapters without per-step
    subagent whitelisting (Cursor) or for inline orchestrator steps in CC.

    Includes script command, schema fields, and example invocation hints
    so the LLM can use the tools correctly via the shell/Bash tool.
    """
    if not active_tools:
        return ""
    sections = ["## Available tools for this step", ""]
    for t in active_tools:
        name = t.get("name", "")
        desc = t.get("description", "")
        ttype = t.get("type", "script")
        if ttype == "script":
            cmd = t.get("command", "")
            schema = t.get("input_schema") or {}
            sections.append(f"### {name}")
            sections.append(f"_{desc}_" if desc else "")
            sections.append("")
            sections.append(f"**Command:** `{cmd}`")
            props = schema.get("properties") or {}
            required = set(schema.get("required") or [])
            if props:
                sections.append("")
                sections.append("**Arguments:**")
                for pname, pdef in props.items():
                    pdesc = (pdef or {}).get("description", "")
                    ptype = (pdef or {}).get("type", "any")
                    req_marker = " _(required)_" if pname in required else ""
                    sections.append(f"- `{pname}` ({ptype}){req_marker}: {pdesc}")
            sections.append("")
            sections.append(
                "Invoke this tool. In Claude Code prefer the MCP form "
                f"`mcp__workflow_tools__{_normalize_for_mcp(workflow_name)}__"
                f"{_normalize_for_mcp(name)}`. In Cursor or as a fallback, "
                f"run the underlying command via the shell tool."
            )
            sections.append("")
        else:
            sections.append(f"### {name} (MCP server)")
            sections.append(f"_{desc}_" if desc else "")
            expected = t.get("expected_tools") or []
            if expected:
                sections.append("")
                sections.append("**Available tools from this server:**")
                for et in expected:
                    sections.append(
                        f"- `mcp__{_normalize_for_mcp(name)}__{_normalize_for_mcp(et)}`"
                    )
            sections.append("")
            sections.append(
                f"This server must be registered in your MCP configuration "
                f"(.mcp.json or .cursor/mcp.json). If the tools are not visible, "
                f"check that the {name} server is enabled."
            )
            sections.append("")
    return "\n".join(sections).rstrip()


def _compose_branch_prompt(branch, step_name, runtime_dir, defn_dir=None):
    """Compose a self-contained markdown prompt file for one parallel branch.

    The subagent reads ONLY this file — it contains everything needed:
    goal, inputs (inlined or as read-instructions), outputs with schemas,
    constraints, and the run token.
    """
    sections = []
    sections.append(f"# Branch {branch['branch_index']} — Step: {step_name}\n")
    sections.append(branch["run_token_text"])

    # Goal (includes branch_data JSON + resolved goal template)
    sections.append("## Goal\n")
    sections.append(branch["goal"])

    # Params
    if branch.get("params_text"):
        sections.append("\n## Parameters\n")
        sections.append(branch["params_text"])

    # Inputs — inline file content or read-instruction
    if branch.get("inputs"):
        sections.append("\n## Inputs\n")
        for inp in branch["inputs"]:
            path = inp.get("path", "")
            inject = inp.get("inject", "reference")
            # Resolve path for reading: root-relative dot-dirs (.user/,
            # .agent/, etc.) and absolute paths read from workspace root;
            # bare relative paths (data/...) read from runtime dir first,
            # then fall back to definition dir (for template assets like
            # structs/*.schema.yaml).
            if os.path.isabs(path) or _is_root_relative(path):
                read_path = path
            else:
                read_path = os.path.join(runtime_dir, path)
                if defn_dir and not os.path.exists(read_path):
                    read_path = os.path.join(defn_dir, path)

            if inject == "file":
                # Read and inline the file content
                try:
                    with open(read_path, "r", encoding="utf-8") as f:
                        content = f.read()
                    sections.append(f"### Input: `{path}`\n")
                    sections.append(f"```\n{content}\n```\n")
                except (FileNotFoundError, OSError):
                    sections.append(
                        f"### Input: `{path}` — ⚠ file not found\n"
                    )
            elif inject == "file_if_exists":
                try:
                    with open(read_path, "r", encoding="utf-8") as f:
                        content = f.read()
                    sections.append(f"### Input: `{path}`\n")
                    sections.append(f"```\n{content}\n```\n")
                except (FileNotFoundError, OSError):
                    pass  # Optional — skip silently
            else:
                # inject: "reference" — subagent reads the file itself
                sections.append(f"- **Read this file yourself:** `{path}`\n")

    # Previous step summaries
    if branch.get("summaries_text"):
        sections.append("\n## Previous Step Summaries\n")
        sections.append(branch["summaries_text"])

    # Outputs (includes inline struct schemas)
    if branch.get("outputs_text"):
        sections.append("\n## Outputs\n")
        sections.append(branch["outputs_text"])

    # Spec check
    if branch.get("spec_check_text"):
        sections.append("\n## Spec Check\n")
        sections.append(branch["spec_check_text"])

    # Constraints
    if branch.get("constraints_text"):
        sections.append("\n## Constraints\n")
        sections.append(branch["constraints_text"])

    # Tool docs (if any workflow tools are available for this step)
    if branch.get("tool_docs"):
        sections.append("\n## Available Tools\n")
        sections.append(branch["tool_docs"])

    return "\n".join(sections)


def _summarize_branch_data(branch_data):
    """Return a short one-line summary of branch data for the slim response."""
    if isinstance(branch_data, dict):
        # Try common keys that identify the branch
        for key in ("name", "domain", "id", "label", "title", "key"):
            if key in branch_data:
                return str(branch_data[key])
        # Fallback: first key-value pair
        for k, v in branch_data.items():
            return f"{k}: {v}" if not isinstance(v, (dict, list)) else k
    return str(branch_data)[:100]


def _is_root_relative(path):
    """Check if a path is a root-relative dot-directory (.user/, .agent/, etc.).

    Paths starting with a dot followed by a letter are workspace root-relative
    by convention (e.g., .user/docs/, .agent/specs/, .cursor/rules/).
    These should NOT be prefixed with the workflow runtime directory.

    Does NOT match:
    - "./foo" (current dir relative)
    - "../foo" (parent dir relative)
    - ".hidden" (hidden file, no slash)
    """
    return bool(path) and path[0] == '.' and len(path) > 1 and path[1].isalpha()


def tool_step_begin(arguments):
    """Prepare a step for execution.

    Returns pre-composed text blocks for subagent task composition.
    Also updates manifest to mark step as in_progress.

    For delegation steps, returns delegation metadata instead.
    """
    workflow_name = arguments.get("name") or arguments.get("workflow_name")
    step_name = arguments.get("step") or arguments.get("step_name")

    defn_dir, wf = resolve_workflow(workflow_name)
    runtime_dir = os.path.join(WORKFLOWS_DIR, workflow_name)
    manifest_path = os.path.join(runtime_dir, "manifest.json")

    with open(manifest_path, "r", encoding="utf-8") as f:
        manifest = json.load(f)

    # Find step config in workflow definition
    step_config = None
    step_index = 0
    for i, s in enumerate(wf.get("steps", [])):
        if s["name"] == step_name:
            step_config = s
            step_index = i
            break

    if not step_config:
        return {
            "error": f"Step '{step_name}' not found in workflow '{workflow_name}'"
        }

    # Guard: warn if step is already completed (possible re-run)
    existing_status = manifest.get("steps", {}).get(step_name, {}).get("status")
    if existing_status == "completed":
        _log(f"Warning: step '{step_name}' is already completed — re-running")

    params = manifest.get("params", {})

    # ── Delegation step ──
    if "delegate_to" in step_config:
        delegate_params = step_config.get("params", {})
        resolved = {}
        for k, v in delegate_params.items():
            resolved[k] = _resolve_placeholders(v, params) if isinstance(v, str) else v

        # Update manifest: mark step as in_progress (merge, don't overwrite)
        now = datetime.now(timezone.utc).astimezone().isoformat()
        step_data = manifest.get("steps", {}).get(step_name, {})
        step_data["status"] = "in_progress"
        step_data["started_at"] = now
        manifest.setdefault("steps", {})[step_name] = step_data
        manifest["current_step"] = step_name
        manifest["updated_at"] = now
        _safe_write_json(manifest_path, manifest)

        return {
            "type": "delegation",
            "step_name": step_name,
            "step_index": step_index,
            "delegate_to": step_config["delegate_to"],
            "resolved_params": resolved,
            "goal": step_config.get("goal", ""),
            "param_bindings": step_config.get("param_bindings", {}),
            "model": step_config.get("model"),  # Optional model hint (opaque string)
        }

    # ── Regular step: compose text blocks ──
    run_token = manifest.get("run_token", "")

    # (a) Params text
    params_parts = []
    for pname, pval in params.items():
        if pval is not None:
            params_parts.append(f'{pname} = "{pval}"')
        else:
            params_parts.append(f"{pname} = (not specified)")
    params_text = "**Params:** " + ", ".join(params_parts) if params_parts else ""

    # (b) Goal with resolved placeholders
    goal = step_config.get("goal", "")
    goal_resolved = _resolve_placeholders(goal, params)

    # (c) Inputs
    # Glob patterns (*, ?, [) are resolved here in Python so the
    # orchestrator doesn't need IDE search tools (which skip gitignored
    # paths like .agent/).  Each matched file becomes a separate entry.
    inputs_info = []
    for inp in step_config.get("inputs", []):
        if isinstance(inp, dict):
            path = inp.get("path", "")
            inject = inp.get("inject", "reference")
            struct = inp.get("struct")
        else:
            path = str(inp)
            inject = "reference"
            struct = None

        resolved_path = _resolve_placeholders(path, params)

        # Detect glob patterns and expand them
        has_glob = any(c in resolved_path for c in ("*", "?", "["))
        if has_glob:
            # Prefix bare relative globs with runtime_dir so they search
            # the correct directory (e.g., data/drafts/**/*.md searches
            # inside .agent/workflows/<name>/data/drafts/).
            # Root-relative (.agent/, .user/) and absolute globs search
            # from workspace root as-is.
            if not _is_root_relative(resolved_path) and not os.path.isabs(resolved_path):
                glob_pattern = os.path.join(runtime_dir, resolved_path)
                matched = sorted(glob_module.glob(glob_pattern, recursive=True))
                # Fallback: try defn_dir for template assets (structs/)
                if not matched and defn_dir:
                    glob_pattern = os.path.join(defn_dir, resolved_path)
                    matched = sorted(glob_module.glob(glob_pattern, recursive=True))
            else:
                matched = sorted(glob_module.glob(resolved_path, recursive=True))
            if not matched and inject == "file_if_exists":
                # No matches for optional input — skip silently
                continue
            for match_path in matched:
                mp = match_path.replace("\\", "/")
                inputs_info.append(entry := {"path": mp, "inject": inject})
                if struct:
                    entry["struct"] = struct
        else:
            # Non-glob path: apply path prefixing as before.
            # Root-relative dot-dirs (.user/, .cursor/, .claude/, .agent/)
            # and absolute paths are kept as-is. Only bare relative paths
            # (like "data/report.json") get prefixed with the runtime dir;
            # if not found there, fall back to the definition dir (for
            # template assets like structs/*.schema.yaml).
            if not _is_root_relative(resolved_path) and not os.path.isabs(resolved_path):
                candidate = os.path.join(runtime_dir, resolved_path)
                if defn_dir and not os.path.exists(candidate):
                    defn_candidate = os.path.join(defn_dir, resolved_path)
                    if os.path.exists(defn_candidate):
                        candidate = defn_candidate
                resolved_path = os.path.relpath(candidate).replace("\\", "/")
            entry = {"path": resolved_path, "inject": inject}
            if struct:
                entry["struct"] = struct
            inputs_info.append(entry)

    # Dedup inputs by (resolved_path, inject) — multiple workflow.yaml
    # entries can resolve to the same file when scope=generic (e.g.,
    # "{scope}/**/*.md" and "generic/**/*.md" both become "generic/**/*.md").
    # Keep first occurrence; preserve order.
    seen_inputs = set()
    deduped = []
    for inp in inputs_info:
        key = (inp["path"], inp["inject"])
        if key not in seen_inputs:
            seen_inputs.add(key)
            deduped.append(inp)
    inputs_info = deduped

    # (d) Outputs text + inline schema content
    outputs_parts = []
    schema_sections = []
    output_schemas = []  # structured metadata for orchestrator
    for out in step_config.get("outputs", []):
        if isinstance(out, dict):
            opath = out.get("path", "")
            ostruct = out.get("struct")
        else:
            opath = str(out)
            ostruct = None

        opath_resolved = _resolve_placeholders(opath, params)
        # Make paths explicit for the agent:
        # Relative paths (e.g., "data/result.json") → prefix with runtime dir
        # Root-relative dot-dirs (.agent/, .user/) and absolute paths → keep as-is
        if not _is_root_relative(opath_resolved) and not os.path.isabs(opath_resolved):
            opath_for_agent = os.path.join(
                os.path.relpath(runtime_dir), opath_resolved
            ).replace("\\", "/")
        else:
            opath_for_agent = opath_resolved
        if ostruct:
            schema_path = f"{defn_dir}/structs/{ostruct}.schema.yaml"
            # Infer output format from file extension
            ext = os.path.splitext(opath_resolved)[1].lower()
            format_map = {
                ".json": "JSON", ".yaml": "YAML", ".yml": "YAML",
                ".md": "Markdown",
            }
            fmt = format_map.get(ext, "JSON")
            outputs_parts.append(
                f"- `{opath_for_agent}` — **{fmt}** file, "
                f"struct: `{ostruct}`"
            )
            # Read schema content for inline inclusion
            schema_content = None
            try:
                with open(schema_path, "r", encoding="utf-8") as sf:
                    schema_content = sf.read().strip()
                schema_sections.append(
                    f"**Struct `{ostruct}`** (output: `{opath_for_agent}`):\n"
                    f"```yaml\n{schema_content}\n```\n"
                    f"Write as **{fmt}** matching this schema exactly."
                )
            except FileNotFoundError:
                schema_sections.append(
                    f"**Struct `{ostruct}`**: ⚠ schema file not found "
                    f"at `{schema_path}` — ask the user for guidance."
                )
            output_schemas.append({
                "path": opath_for_agent,
                "struct": ostruct,
                "format": fmt,
                "schema_path": schema_path,
                "content": schema_content,
            })
        else:
            outputs_parts.append(f"- `{opath_for_agent}`")

    outputs_text = ""
    if outputs_parts:
        outputs_text = "**Write outputs to:**\n" + "\n".join(outputs_parts)
        if schema_sections:
            outputs_text += (
                "\n\n**Output schemas — your output MUST match "
                "these exactly:**\n\n"
                + "\n\n".join(schema_sections)
            )

    # (e) Summaries (carry_forward)
    summaries_text = ""
    wf_carry = wf.get("context", {}).get("carry_forward", "none")
    step_carry = step_config.get("carry_forward")  # True, False, or None

    should_carry = False
    if step_carry is False:
        should_carry = False
    elif step_carry is True:
        # Step explicitly opts in — override workflow-level setting
        should_carry = True
    elif wf_carry == "summary":
        # step_carry is None (defaults to True when wf-level is "summary")
        should_carry = True

    if should_carry:
        summary_parts = []
        # Use workflow definition order, only include steps before current
        step_order = [s["name"] for s in wf.get("steps", [])]
        for sname in step_order:
            if sname == step_name:
                break  # Only previous steps
            sdata = manifest.get("steps", {}).get(sname, {})
            if sdata.get("status") == "completed":
                summary_path = os.path.join(
                    runtime_dir, "context", f"{sname}.summary.md"
                )
                if os.path.isfile(summary_path):
                    with open(summary_path, "r", encoding="utf-8") as f:
                        content = f.read().strip()
                    summary_parts.append(
                        f"**Previous step '{sname}' summary:** {content}"
                    )
        if summary_parts:
            summaries_text = "\n\n".join(summary_parts)

    # (f) Spec check
    spec_check_text = ""
    if step_config.get("spec_check", True):
        spec_check_text = (
            "**Spec check:** Before making code changes, read "
            "`.agent/specs/_registry.json` to find specs whose "
            "`implemented_by` files overlap with files you plan to modify. "
            "Read each relevant spec and verify your changes do NOT violate "
            "any requirement. If a violation is found: STOP and report the "
            "conflict in your summary (do NOT proceed with conflicting "
            "changes). After completing changes, verify no specs were violated."
        )

    # (g) Constraints
    constraints_text = (
        "**Constraints:**\n"
        "- Write outputs ONLY to the specified paths.\n"
        "- Do NOT modify files outside the output paths.\n"
        "- NEVER modify engine/infrastructure files: `.agent/scripts/`, "
        "`.agent/docs/`, `.agent/tools/`, `.cursor/skills/`, "
        "`.cursor/rules/`, `.cursor/hooks.json`. These are read-only "
        "system files. If you encounter errors caused by these files, "
        "report the problem in your summary — do NOT attempt to fix them.\n"
        "- When done, summarize what you did in 2-3 sentences."
    )

    # (h) Run token
    run_token_text = f"<!--workflow:run_token:{run_token}-->"

    # ── Clear stale gate-result.json from previous step ──
    # gate-result.json is a single shared file overwritten by gate-check.py.
    # If not cleared, step_collect_result may read a result from a previous
    # step and emit a stale-data warning.
    gate_result_path = os.path.join(runtime_dir, "gate-result.json")
    if os.path.isfile(gate_result_path):
        try:
            os.remove(gate_result_path)
        except OSError:
            pass  # Best effort — gate-check.py will overwrite anyway

    # ── Update manifest: mark step as in_progress (merge, don't overwrite) ──
    now = datetime.now(timezone.utc).astimezone().isoformat()
    step_data = manifest.get("steps", {}).get(step_name, {})
    step_data["status"] = "in_progress"
    step_data["started_at"] = now
    manifest.setdefault("steps", {})[step_name] = step_data
    manifest["current_step"] = step_name
    manifest["updated_at"] = now
    _safe_write_json(manifest_path, manifest)

    # (i) Subagent flag — False means orchestrator should execute inline
    use_subagent = step_config.get("subagent", True)

    # (j) Active tools for this step (script + mcp)
    # Phase 38: each step's subagent is a pre-generated subagent type with a
    # strict tools whitelist (.claude/agents/<workflow>-<step>/AGENT.md). The
    # orchestrator uses subagent_type to spawn the correct one. For inline
    # steps and Cursor adapters, we also surface a tool_docs block that the
    # orchestrator can append to the step prompt.
    active_tools = []
    wf_tools = wf.get("tools", [])
    if wf_tools:
        step_tools_filter = step_config.get("tools")
        if step_tools_filter:
            active_tools = [
                t for t in wf_tools
                if t.get("name") in step_tools_filter
            ]
        else:
            active_tools = list(wf_tools)

    # Compose tool_docs for prompt enrichment.
    # Adapter detection: if .claude/agents/<workflow>-<step>/AGENT.md exists
    # we are in CC mode and the subagent already sees the tools via whitelist.
    # In Cursor mode (or for inline steps even in CC), we provide full docs.
    per_step_subagent_dir = os.path.join(
        ".claude", "agents", f"{workflow_name}-{step_name}"
    )
    is_cc_per_step = (
        use_subagent
        and os.path.isfile(os.path.join(per_step_subagent_dir, "AGENT.md"))
    )

    tool_docs_block = ""
    if active_tools:
        if is_cc_per_step:
            # Brief affordance — the subagent already has the tools whitelisted
            tool_docs_block = _render_tool_docs_brief(active_tools, workflow_name)
        else:
            # Full inline docs (Cursor adapter or inline orchestrator step)
            tool_docs_block = _render_tool_docs_full(active_tools, workflow_name)

    # Per-step subagent type (CC only). Falls back to "workflow-step" when
    # the per-step file does not exist (Cursor, or workflow not yet initialized
    # via /init-workflow).
    if use_subagent:
        if is_cc_per_step:
            subagent_type = f"{workflow_name}-{step_name}"
        else:
            subagent_type = "workflow-step"
    else:
        subagent_type = None

    is_parallel = step_config.get("parallel", False)
    parallel_key_raw = step_config.get("parallel_key")

    # ── Parallel branch resolution ──
    # For parallel steps: resolve branches, create per-branch isolated output
    # paths and task prompts. Gate-check validates only the branch's files.
    if is_parallel and parallel_key_raw:
        try:
            branch_items = _resolve_parallel_branches(
                parallel_key_raw, step_config, runtime_dir
            )
        except (ValueError, KeyError, json.JSONDecodeError) as e:
            return {"error": f"Failed to resolve parallel branches: {e}"}

        if not branch_items:
            return {"error": f"parallel_key '{parallel_key_raw}' resolved to 0 items"}

        # Build per-branch prompts with isolated output dirs
        branches = []
        # Determine base output dir from first output path template
        raw_outputs = step_config.get("outputs", [])
        for branch_index, branch_item in enumerate(branch_items):
            branch_dir = f"_branch_{branch_index}"

            # Rewrite output paths: insert _branch_N/ after "data/drafts/"
            # or after the first path component that contains a {variable}
            branch_outputs_parts = []
            branch_schema_sections = []
            branch_output_schemas = []
            for out in raw_outputs:
                if isinstance(out, dict):
                    opath = out.get("path", "")
                    ostruct = out.get("struct")
                else:
                    opath = str(out)
                    ostruct = None

                # Replace {variable} segments with branch dir. The branch
                # dir is a sandbox — subagent writes freely inside it.
                # E.g.: "data/drafts/{domain}/*.md" → "data/drafts/_branch_0/*.md"
                #        "data/{region}/{city}/*.json" → "data/_branch_0/*.json"
                parts = opath.split("/")
                new_parts = []
                found_var = False
                for p in parts:
                    if "{" in p:
                        if not found_var:
                            new_parts.append(branch_dir)
                            found_var = True
                        # Skip all {variable} segments — collapsed into branch dir
                    else:
                        new_parts.append(p)
                if not found_var:
                    new_parts.insert(-1, branch_dir)
                branch_opath = "/".join(new_parts)

                branch_opath_resolved = _resolve_placeholders(branch_opath, params)
                if not _is_root_relative(branch_opath_resolved) and not os.path.isabs(branch_opath_resolved):
                    branch_opath_for_agent = os.path.join(
                        os.path.relpath(runtime_dir), branch_opath_resolved
                    ).replace("\\", "/")
                else:
                    branch_opath_for_agent = branch_opath_resolved

                if ostruct:
                    schema_path = f"{defn_dir}/structs/{ostruct}.schema.yaml"
                    ext = os.path.splitext(branch_opath_resolved)[1].lower()
                    format_map = {
                        ".json": "JSON", ".yaml": "YAML", ".yml": "YAML",
                        ".md": "Markdown",
                    }
                    fmt = format_map.get(ext, "JSON")
                    branch_outputs_parts.append(
                        f"- `{branch_opath_for_agent}` — **{fmt}** file, "
                        f"struct: `{ostruct}`"
                    )
                    try:
                        with open(schema_path, "r", encoding="utf-8") as sf:
                            schema_content = sf.read().strip()
                        branch_schema_sections.append(
                            f"**Struct `{ostruct}`** (output: `{branch_opath_for_agent}`):\n"
                            f"```yaml\n{schema_content}\n```\n"
                            f"Write as **{fmt}** matching this schema exactly."
                        )
                    except FileNotFoundError:
                        branch_schema_sections.append(
                            f"**Struct `{ostruct}`**: schema not found at `{schema_path}`"
                        )
                    branch_output_schemas.append({
                        "path": branch_opath_for_agent,
                        "struct": ostruct, "format": fmt,
                    })
                else:
                    branch_outputs_parts.append(f"- `{branch_opath_for_agent}`")

            branch_outputs_text = ""
            if branch_outputs_parts:
                branch_outputs_text = (
                    "**Write outputs to:**\n" + "\n".join(branch_outputs_parts)
                )
                if branch_schema_sections:
                    branch_outputs_text += (
                        "\n\n**Output schemas — your output MUST match "
                        "these exactly:**\n\n"
                        + "\n\n".join(branch_schema_sections)
                    )

            # Branch context: provide the branch item as JSON for the subagent
            branch_context = json.dumps(branch_item, indent=2, ensure_ascii=False)
            branch_goal = (
                f"**Your assigned parallel branch (#{branch_index}):**\n"
                f"```json\n{branch_context}\n```\n\n"
                f"{goal_resolved}"
            )

            branch_run_token = f"<!--workflow:run_token:{run_token}--><!--workflow:branch:{branch_index}-->"

            branches.append({
                "branch_index": branch_index,
                "branch_data": branch_item,
                "branch_dir": branch_dir,
                "goal": branch_goal,
                "outputs_text": branch_outputs_text,
                "output_schemas": branch_output_schemas,
                "tool_docs": tool_docs_block,
                "params_text": params_text,
                "inputs": inputs_info,
                "summaries_text": summaries_text,
                "spec_check_text": spec_check_text,
                "constraints_text": constraints_text,
                "run_token_text": branch_run_token,
                "subagent_type": subagent_type,
                "model": step_config.get("model"),
            })

        # ── Write per-branch prompt files ──
        # Each file is self-contained: the subagent reads ONLY its file.
        # Files persist after step completion for debugging and trace.
        prompts_dir = os.path.join(runtime_dir, "data", "prompts")
        os.makedirs(prompts_dir, exist_ok=True)
        prompt_files = []
        slim_branches = []
        for branch in branches:
            prompt_content = _compose_branch_prompt(
                branch, step_name, runtime_dir, defn_dir
            )
            prompt_filename = f"branch_{branch['branch_index']}.md"
            prompt_path = os.path.join(prompts_dir, prompt_filename)
            _safe_write_text(prompt_path, prompt_content)

            # Relative path from project root for the orchestrator/agent
            prompt_rel = os.path.join(
                os.path.relpath(prompts_dir), prompt_filename
            ).replace("\\", "/")
            prompt_files.append(prompt_rel)

            # Ready-to-use spawn prompt: orchestrator passes this directly
            # to the Agent/Task tool. Contains the prompt file instruction
            # AND the run_token + branch marker so the gate hook can match.
            spawn_prompt = (
                f"Read `{prompt_rel}` and execute the task described in it.\n\n"
                f"{branch['run_token_text']}"
            )

            slim_branches.append({
                "branch_index": branch["branch_index"],
                "prompt_file": prompt_rel,
                "spawn_prompt": spawn_prompt,
                "subagent_type": branch["subagent_type"],
                "model": branch["model"],
                "summary": _summarize_branch_data(branch["branch_data"]),
            })

        # Write branch info to manifest for gate-check
        manifest["parallel_branches"] = {
            str(i): {"branch_dir": f"_branch_{i}", "status": "pending"}
            for i in range(len(branch_items))
        }
        # Record prompt file paths for trace
        step_data = manifest.setdefault("steps", {}).get(step_name, {})
        step_data["prompt_files"] = prompt_files
        manifest.setdefault("steps", {})[step_name] = step_data
        _safe_write_json(manifest_path, manifest)

        result = {
            "type": "parallel",
            "step_name": step_name,
            "step_index": step_index,
            "subagent": use_subagent,
            "branch_count": len(slim_branches),
            "prompt_dir": os.path.relpath(prompts_dir).replace("\\", "/"),
            "branches": slim_branches,
        }
    else:
        result = {
            "type": "regular",
            "step_name": step_name,
            "step_index": step_index,
            "subagent": use_subagent,
            "subagent_type": subagent_type,
            "goal": goal_resolved,
            "tool_docs": tool_docs_block,
            "params_text": params_text,
            "inputs": inputs_info,
            "outputs_text": outputs_text,
            "output_schemas": output_schemas,
            "summaries_text": summaries_text,
            "spec_check_text": spec_check_text,
            "constraints_text": constraints_text,
            "run_token_text": run_token_text,
            "is_parallel": False,
            "parallel_key": None,
            "model": step_config.get("model"),
        }
    if active_tools and use_subagent and not is_cc_per_step:
        # The orchestrator should be told that the workflow has tools but the
        # per-step subagent file is missing — usually means /init-workflow has
        # not been run since the workflow was created or edited.
        result["subagent_warning"] = (
            f"Per-step subagent '{workflow_name}-{step_name}' was not found "
            f"in .claude/agents/. Falling back to generic 'workflow-step', "
            f"which has no whitelisted MCP tools for this step. "
            f"Run /init-workflow {workflow_name} (then restart Claude Code) "
            f"to generate it."
        )
    return result


def tool_step_collect_result(arguments):
    """Read gate-result.json after subagent completes. Determine next action."""
    workflow_name = arguments.get("name") or arguments.get("workflow_name")
    step_name = arguments.get("step") or arguments.get("step_name")

    runtime_dir = os.path.join(WORKFLOWS_DIR, workflow_name)
    result_path = os.path.join(runtime_dir, "gate-result.json")
    manifest_path = os.path.join(runtime_dir, "manifest.json")

    # Check if step is inline (subagent: false) — gate won't fire
    if step_name:
        defn_dir_check, wf_check = resolve_workflow(workflow_name)
        for s in wf_check.get("steps", []):
            if s["name"] == step_name and s.get("subagent") is False:
                return {
                    "warning": (
                        f"Step '{step_name}' has subagent: false — "
                        "no gate hook fires for inline steps. "
                        "Skip step_collect_result and proceed to step_complete."
                    ),
                    "action": "PROCEED",
                    "passed": True,
                    "needs_semantic_gate": False,
                    "needs_human_gate": False,
                }

    if not os.path.isfile(result_path):
        return {
            "error": (
                "gate-result.json not found — gate-check.py may not have "
                "executed. Check .agent/gate-check-invocations.log and "
                ".agent/gate-check-error.log for diagnostics."
            ),
            "action": "STOP_WORKFLOW",
        }

    with open(result_path, "r", encoding="utf-8") as f:
        gate_result = json.load(f)

    # Verify the result is for the current run (not stale from a previous
    # session or run). gate-check.py writes run_token from manifest into
    # gate-result.json. If tokens don't match → stale result.
    with open(manifest_path, "r", encoding="utf-8") as f:
        manifest = json.load(f)
    manifest_token = manifest.get("run_token", "")
    result_token = gate_result.get("run_token", "")
    if manifest_token and result_token and result_token != manifest_token:
        return {
            "warning": (
                f"gate-result.json has run_token '{result_token[:8]}...' "
                f"but current run is '{manifest_token[:8]}...'. "
                f"The result is stale (from a previous run or session). "
                f"Proceed to step_complete — its safety net will validate "
                f"outputs directly."
            ),
            "action": "PROCEED",
            "passed": True,
            "stale": True,
            "needs_semantic_gate": True,
            "needs_human_gate": False,
        }

    # Verify the result is for the expected step
    result_step = gate_result.get("step", "")
    if step_name and result_step != step_name:
        return {
            "warning": (
                f"gate-result.json is for step '{result_step}', "
                f"but expected '{step_name}'. The result may be stale."
            ),
            "gate_result": gate_result,
            "action": "VERIFY_MANUALLY",
        }

    passed = gate_result.get("passed", False)

    if passed:
        # Determine if semantic/human gates are needed
        defn_dir, wf = resolve_workflow(workflow_name)
        step_config = None
        for s in wf.get("steps", []):
            if s["name"] == (step_name or result_step):
                step_config = s
                break

        wf_gate = wf.get("gate", {})
        step_gate = (step_config or {}).get("gate", {})

        semantic = step_gate.get("semantic", wf_gate.get("semantic", False))
        human = step_gate.get("human", wf_gate.get("human", False))

        result = {
            "passed": True,
            "step": result_step,
            "action": "PROCEED",
            "checks": gate_result.get("checks", 0),
            "details": gate_result.get("details", []),
            "needs_semantic_gate": semantic,
            "needs_human_gate": human,
        }
        # Forward per-branch results for parallel steps so the
        # orchestrator can detect and retry specific failed branches.
        if "branch_results" in gate_result:
            result["branch_results"] = gate_result["branch_results"]
        if "failed_branches" in gate_result:
            result["failed_branches"] = gate_result["failed_branches"]
        return result
    else:
        # Read manifest for retry config
        defn_dir, wf = resolve_workflow(workflow_name)
        wf_gate = wf.get("gate", {})
        step_config = None
        for s in wf.get("steps", []):
            if s["name"] == (step_name or result_step):
                step_config = s
                break
        step_gate = (step_config or {}).get("gate", {})
        max_step_retries = step_gate.get(
            "max_step_retries", wf_gate.get("max_step_retries", 3)
        )

        return {
            "passed": False,
            "step": result_step,
            "action": "RETRY_STEP",
            "checks": gate_result.get("checks", 0),
            "failures": gate_result.get("failures", 0),
            "details": gate_result.get("details", []),
            "loop_count": gate_result.get("loop_count", 0),
            "max_step_retries": max_step_retries,
        }


def _validate_step_outputs(step_config, defn_dir, runtime_dir):
    """Validate step outputs against struct schemas (same logic as gate-check.py).

    Called for inline steps (subagent: false) where the SubagentStop hook
    does not fire. Returns (passed, errors) where errors is a list of strings.
    """
    outputs = step_config.get("outputs", [])
    if not outputs:
        return True, []

    structs_dir = os.path.join(defn_dir, "structs")
    schema_validate = os.path.join(os.getcwd(), ".agent", "scripts", "schema-validate.py")
    errors = []

    for out in outputs:
        if not isinstance(out, dict):
            continue
        struct_name = out.get("struct")
        if not struct_name:
            continue

        output_path = out.get("path", "")
        schema_path = os.path.join(structs_dir, f"{struct_name}.schema.yaml")

        if not os.path.isfile(schema_path):
            errors.append(f"Schema not found: {schema_path}")
            continue

        # Resolve output files (glob for {variable} placeholders)
        glob_pattern = re.sub(r"\{[^}]+\}", "*", output_path)
        abs_pattern = os.path.join(runtime_dir, glob_pattern)
        files = glob_module.glob(abs_pattern, recursive=True)

        if not files:
            errors.append(f"Output file not found: {output_path}")
            continue

        for file_path in files:
            try:
                result = subprocess.run(
                    [sys.executable, schema_validate, file_path, schema_path],
                    capture_output=True, text=True, timeout=30,
                )
                if result.returncode != 0:
                    errors.append(
                        f"{os.path.basename(file_path)}: {result.stderr.strip()}"
                    )
                    continue
                validation = json.loads(result.stdout)
                for detail in validation.get("details", []):
                    if "FAIL" in detail or "missing" in detail.lower():
                        errors.append(detail)
            except subprocess.TimeoutExpired:
                errors.append(f"Validation timeout for {os.path.basename(file_path)}")
            except Exception as e:
                errors.append(f"Validation error: {e}")

    return len(errors) == 0, errors


def tool_step_complete(arguments):
    """Mark step as completed. Write summary, apply param_bindings, advance."""
    workflow_name = arguments.get("name") or arguments.get("workflow_name")
    step_name = arguments.get("step") or arguments.get("step_name")
    summary = arguments.get("summary", "")

    defn_dir, wf = resolve_workflow(workflow_name)
    runtime_dir = os.path.join(WORKFLOWS_DIR, workflow_name)
    manifest_path = os.path.join(runtime_dir, "manifest.json")

    with open(manifest_path, "r", encoding="utf-8") as f:
        manifest = json.load(f)

    # ── Find step config (needed for validation + param_bindings) ──
    step_config = None
    for s in wf.get("steps", []):
        if s["name"] == step_name:
            step_config = s
            break

    # ── Output validation (safety net) ──
    # Validates outputs BEFORE marking the step as completed.
    # Runs when the SubagentStop hook gate did NOT fire:
    #   - subagent: false → no subagent, no hook
    #   - parallel steps → Claude Code may not fire hook for parallel agents
    #   - any case where gate-result.json is missing after step execution
    #
    # If the hook DID fire (gate-result.json exists for this step),
    # validation was already done by gate-check.py — skip it here.
    if step_config:
        has_structs = any(
            isinstance(o, dict) and o.get("struct")
            for o in step_config.get("outputs", [])
        )
        if has_structs:
            # Check if gate already validated this step
            gate_result_path = os.path.join(runtime_dir, "gate-result.json")
            gate_already_ran = False
            if os.path.isfile(gate_result_path):
                try:
                    with open(gate_result_path, "r", encoding="utf-8") as gf:
                        gr = json.load(gf)
                    if gr.get("step") == step_name and gr.get("passed"):
                        gate_already_ran = True
                except (json.JSONDecodeError, OSError):
                    pass

            if not gate_already_ran:
                passed, errors = _validate_step_outputs(
                    step_config, defn_dir, runtime_dir
                )
                if not passed:
                    return {
                        "error": (
                            f"Output validation failed for step "
                            f"'{step_name}': {'; '.join(errors)}"
                        ),
                        "action": "FIX_AND_RETRY",
                        "step": step_name,
                        "validation_errors": errors,
                    }

    # ── Parallel branch merge ──
    # If this step had parallel branches, merge isolated output dirs
    # to the final location before marking complete.
    parallel_branches = manifest.get("parallel_branches")
    if parallel_branches and step_config and step_config.get("parallel"):
        raw_outputs = step_config.get("outputs", [])
        for out in raw_outputs:
            opath = out.get("path", "") if isinstance(out, dict) else str(out)
            opath_resolved = _resolve_placeholders(opath, manifest.get("params", {}))
            # Find the base dir (before first {variable})
            parts = opath_resolved.split("/")
            base_parts = []
            for p in parts:
                if "{" in p or "*" in p:
                    break
                base_parts.append(p)
            base_dir = os.path.join(runtime_dir, *base_parts) if base_parts else runtime_dir

            # Merge each _branch_N/ subdir into base_dir
            for branch_key, branch_info in parallel_branches.items():
                branch_dir_name = branch_info.get("branch_dir", f"_branch_{branch_key}")
                branch_src = os.path.join(base_dir, branch_dir_name)
                if not os.path.isdir(branch_src):
                    continue
                # Walk branch dir and move files to base_dir preserving structure
                for root, dirs, files in os.walk(branch_src):
                    rel = os.path.relpath(root, branch_src)
                    dest_dir = os.path.join(base_dir, rel) if rel != "." else base_dir
                    os.makedirs(dest_dir, exist_ok=True)
                    for fname in files:
                        src_file = os.path.join(root, fname)
                        dst_file = os.path.join(dest_dir, fname)
                        shutil.move(src_file, dst_file)
                # Remove empty branch dir
                shutil.rmtree(branch_src, ignore_errors=True)

        # Clean up manifest
        del manifest["parallel_branches"]

    # ── Normalize parallel_key filter fields in output data ──
    # If the NEXT step has a parallel_key with a filter (e.g.,
    # "analysis-report.domains[status=pending]"), ensure the filter
    # field is set correctly in the output data file. This prevents
    # LLM subagents from breaking parallel resolution by setting
    # wrong values (e.g., "completed" instead of "pending").
    if step_config:
        step_names = [s["name"] for s in wf.get("steps", [])]
        step_idx = step_names.index(step_name) if step_name in step_names else -1
        if step_idx >= 0 and step_idx + 1 < len(step_names):
            next_step_config = wf["steps"][step_idx + 1]
            pk = next_step_config.get("parallel_key", "")
            pk_match = re.match(
                r'^([^.]+)\.(.+?)(?:\[(\w+)=(\w+)\])?$', pk
            ) if pk else None
            if pk_match and pk_match.group(3) and pk_match.group(4):
                struct_name = pk_match.group(1)
                field_path = pk_match.group(2)
                filter_key = pk_match.group(3)
                filter_val = pk_match.group(4)
                data_file = os.path.join(runtime_dir, "data", f"{struct_name}.json")
                if os.path.isfile(data_file):
                    try:
                        with open(data_file, "r", encoding="utf-8") as df:
                            data = json.load(df)
                        # Navigate to the array
                        items = data
                        for key in field_path.split("."):
                            if isinstance(items, dict) and key in items:
                                items = items[key]
                            else:
                                items = None
                                break
                        if isinstance(items, list):
                            fixed = 0
                            for item in items:
                                if isinstance(item, dict) and item.get(filter_key) != filter_val:
                                    item[filter_key] = filter_val
                                    fixed += 1
                            if fixed:
                                _safe_write_json(data_file, data)
                                _log(f"Normalized {fixed} items: "
                                     f"{filter_key}→{filter_val} in {data_file}")
                    except (json.JSONDecodeError, OSError) as e:
                        _log(f"Warning: failed to normalize parallel_key data: {e}")

    now = datetime.now(timezone.utc).astimezone().isoformat()

    # ── Update step status ──
    if step_name not in manifest.get("steps", {}):
        manifest.setdefault("steps", {})[step_name] = {}
    manifest["steps"][step_name]["status"] = "completed"
    manifest["steps"][step_name]["completed_at"] = now
    manifest["steps"][step_name]["summary"] = summary
    manifest["updated_at"] = now

    # ── Compute duration_ms (if no hook invocations recorded it) ──
    step_started = manifest["steps"][step_name].get("started_at")
    if step_started:
        try:
            start_dt = datetime.fromisoformat(step_started)
            end_dt = datetime.fromisoformat(now)
            manifest["steps"][step_name]["duration_ms"] = int(
                (end_dt - start_dt).total_seconds() * 1000
            )
        except (ValueError, TypeError):
            pass

    # ── Tag tool call entries with step name ──
    tool_calls_path = os.path.join(runtime_dir, "tool-calls.json")
    if os.path.isfile(tool_calls_path):
        try:
            with open(tool_calls_path, "r", encoding="utf-8") as f:
                all_calls = json.load(f)
            # Tag untagged entries (those without "step" field) with current step
            changed = False
            for entry in all_calls:
                if "step" not in entry:
                    entry["step"] = step_name
                    entry["source"] = "tool_server"
                    changed = True
            if changed:
                with open(tool_calls_path, "w", encoding="utf-8") as f:
                    json.dump(all_calls, f, indent=2, ensure_ascii=False)
        except (json.JSONDecodeError, OSError):
            pass

    # ── Verify tool usage for this step ──
    #
    # Warnings policy (Phase 38):
    #   - Only emit warnings when the step EXPLICITLY declares `tools: [...]`.
    #     Without an explicit filter, the step has no contract about which
    #     tools it should call — emitting warnings would be a false positive.
    #   - Only run the check for subagent steps. For inline steps the
    #     orchestrator runs the work itself, and there is no transcript that
    #     gate-check.py can read; we have no visibility into MCP calls made
    #     from inside the orchestrator session.
    step_tool_warnings = []
    is_subagent_step = step_config.get("subagent", True) is not False
    if step_config and is_subagent_step:
        wf_tools = wf.get("tools", [])
        step_tools_filter = step_config.get("tools")  # per-step restriction

        if wf_tools and step_tools_filter:
            expected_tools = [t for t in wf_tools if t["name"] in step_tools_filter]

            if expected_tools:
                # Read tool calls for this step
                step_calls = []
                if os.path.isfile(tool_calls_path):
                    try:
                        with open(tool_calls_path, "r", encoding="utf-8") as f:
                            all_tc = json.load(f)
                        step_calls = [c for c in all_tc if c.get("step") == step_name]
                    except (json.JSONDecodeError, OSError):
                        pass

                # Phase 38: gate-check.py records MCP tool calls as
                # `tool` = "mcp__workflow_tools__<wf>__<tool>" and
                # `tool_short` = "<tool>". Older proxy entries used the
                # bare tool name. Build a set that contains both forms so
                # the comparison below matches either convention.
                called_tools = set()
                for c in step_calls:
                    full = c.get("tool") or ""
                    short = c.get("tool_short") or (full.split("__")[-1] if full else "")
                    if full:
                        called_tools.add(full)
                    if short:
                        called_tools.add(short)

                for tool in expected_tools:
                    tname = tool["name"]
                    ttype = tool.get("type", "script")

                    if ttype == "script":
                        # The Phase 38 loader exposes tools as
                        # mcp__workflow_tools__<workflow>__<tool>. Compare
                        # against both the bare name and the prefixed name.
                        wf_name_norm = re.sub(
                            r"[^a-zA-Z0-9_]", "_", wf.get("name", workflow_name)
                        )
                        tname_norm = re.sub(r"[^a-zA-Z0-9_]", "_", tname)
                        prefixed = (
                            f"mcp__workflow_tools__{wf_name_norm}__{tname_norm}"
                        )
                        if tname not in called_tools and prefixed not in called_tools:
                            step_tool_warnings.append({
                                "tool": tname,
                                "type": "script",
                                "warning": f"Script tool '{tname}' was declared but never called. "
                                           f"The workflow_tools loader exposes it as "
                                           f"'{prefixed}' — check the subagent's tools whitelist.",
                            })
                    elif ttype == "mcp":
                        # For MCP tools, check if any mcp_transcript call references this server
                        mcp_prefix = f"mcp__{tname}__"
                        mcp_called = any(
                            (c.get("tool", "").startswith(mcp_prefix) or
                             c.get("server", "") == tname)
                            for c in step_calls if c.get("source") == "mcp_transcript"
                        )
                        if not mcp_called and tool.get("required", True):
                            step_tool_warnings.append({
                                "tool": tname,
                                "type": "mcp",
                                "warning": f"MCP server '{tname}' was declared (required) but no "
                                           f"tool calls detected. Server may not be running.",
                            })

    # Store warnings in manifest
    if step_tool_warnings:
        manifest["steps"][step_name]["tool_warnings"] = step_tool_warnings
        _log(f"Tool warnings for {step_name}: {len(step_tool_warnings)}")

    # ── Write summary to context/ ──
    context_dir = os.path.join(runtime_dir, "context")
    os.makedirs(context_dir, exist_ok=True)
    summary_path = os.path.join(context_dir, f"{step_name}.summary.md")
    with open(summary_path, "w", encoding="utf-8") as f:
        f.write(summary)

    # ── Apply param_bindings ──
    bindings_applied = {}
    if step_config and step_config.get("param_bindings"):
        for param_name, binding_expr in step_config["param_bindings"].items():
            try:
                file_path, field_path = binding_expr.split("::")

                # Delegation steps: resolve relative to delegated workflow dir
                if "delegate_to" in step_config:
                    delegated_name = step_config["delegate_to"]
                    base_dir = os.path.join(WORKFLOWS_DIR, delegated_name)
                else:
                    base_dir = runtime_dir

                abs_path = os.path.join(base_dir, file_path)
                with open(abs_path, "r", encoding="utf-8") as f:
                    data = json.load(f)

                value = _read_json_field(data, field_path)
                manifest["params"][param_name] = value
                bindings_applied[param_name] = value
            except Exception as e:
                # Don't write error string as param value — keep previous value
                bindings_applied[param_name] = {"error": str(e)}
                _log(f"param_binding '{param_name}' failed: {e}")

    # ── Find next step ──
    step_order = [s["name"] for s in wf.get("steps", [])]
    if step_name not in step_order:
        _log(f"Warning: step '{step_name}' not in workflow definition")
        next_step = None
    else:
        current_idx = step_order.index(step_name)
        next_step = (
            step_order[current_idx + 1]
            if current_idx + 1 < len(step_order)
            else None
        )

    if next_step:
        manifest["current_step"] = next_step

    # ── Write updated manifest ──
    _safe_write_json(manifest_path, manifest)

    result = {
        "step": step_name,
        "status": "completed",
        "next_step": next_step,
        "workflow_done": next_step is None,
        "params": manifest["params"],
    }
    if bindings_applied:
        result["param_bindings_applied"] = bindings_applied
    if step_tool_warnings:
        result["tool_warnings"] = step_tool_warnings

    return result


def tool_list_agent_files(arguments):
    """List files under .agent/ (bypasses gitignore).

    IDE search tools (Glob, Grep) skip gitignored paths, but .agent/
    is gitignored by design. This tool uses Python os.walk to list files
    regardless of gitignore, solving the discovery problem.
    """
    path = arguments.get("path", ".agent/")
    pattern = arguments.get("pattern")  # optional glob filter e.g. "*.md"

    if not os.path.isdir(path):
        return {"files": [], "error": f"Directory not found: {path}"}

    files = []
    for root, _dirs, filenames in os.walk(path):
        for f in filenames:
            if pattern and not _fnmatch(f, pattern):
                continue
            files.append(os.path.join(root, f).replace("\\", "/"))

    return {"files": sorted(files), "count": len(files)}


def _fnmatch(name, pattern):
    """Simple filename pattern match (*, ?)."""
    import fnmatch
    return fnmatch.fnmatch(name, pattern)


def tool_workflow_finalize(arguments):
    """Finalize a completed workflow. Update manifest + trace."""
    workflow_name = arguments.get("name") or arguments.get("workflow_name")

    runtime_dir = os.path.join(WORKFLOWS_DIR, workflow_name)
    manifest_path = os.path.join(runtime_dir, "manifest.json")

    with open(manifest_path, "r", encoding="utf-8") as f:
        manifest = json.load(f)

    now = datetime.now(timezone.utc).astimezone().isoformat()

    # ── Determine final status ──
    all_done = all(
        s.get("status") in ("completed", "skipped")
        for s in manifest.get("steps", {}).values()
    )
    final_status = "completed" if all_done else "failed"

    manifest["status"] = final_status
    manifest["completed_at"] = now
    manifest["updated_at"] = now

    # Mark in_progress steps as failed when workflow itself failed
    if final_status == "failed":
        for sname, sdata in manifest.get("steps", {}).items():
            if sdata.get("status") == "in_progress":
                sdata["status"] = "failed"
                sdata["completed_at"] = now

    _safe_write_json(manifest_path, manifest)

    # ── Finalize trace ──
    defn_dir, wf = resolve_workflow(workflow_name)
    run_id = manifest.get("run_id", "")
    trace_path = os.path.join(runtime_dir, "trace", f"{run_id}.trace.json")

    trace_finalized = False
    if os.path.isfile(trace_path):
        try:
            with open(trace_path, "r", encoding="utf-8") as f:
                trace = json.load(f)

            trace["completed_at"] = now
            trace["status"] = final_status

            # Compute total duration
            started = trace.get("started_at", now)
            try:
                start_dt = datetime.fromisoformat(started)
                end_dt = datetime.fromisoformat(now)
                trace["total_duration_ms"] = int(
                    (end_dt - start_dt).total_seconds() * 1000
                )
            except (ValueError, TypeError):
                pass

            # Aggregate metrics from invocations (subagent steps)
            total_msgs = 0
            total_tools = 0
            all_files = set()
            for step_entry in trace.get("steps", []):
                for inv in step_entry.get("invocations", []):
                    total_msgs += inv.get("message_count", 0)
                    total_tools += inv.get("tool_call_count", 0)
                    for fp in inv.get("modified_files", []):
                        all_files.add(fp)

            # Also aggregate from manifest steps (inline steps have no invocations)
            total_step_duration = 0
            for sname, sdata in manifest.get("steps", {}).items():
                dur = sdata.get("duration_ms")
                if dur and isinstance(dur, (int, float)):
                    total_step_duration += dur

            trace["total_messages"] = total_msgs
            trace["total_tool_calls"] = total_tools
            trace["total_modified_files"] = len(all_files)
            trace["total_step_duration_ms"] = total_step_duration

            # Add synthetic entries for steps missing from trace
            trace_step_names = {s["name"] for s in trace.get("steps", [])}
            for si, s in enumerate(wf.get("steps", [])):
                sname = s["name"]
                if sname not in trace_step_names:
                    mstep = manifest.get("steps", {}).get(sname, {})
                    if mstep.get("status") in ("completed", "skipped"):
                        note = (
                            f"Delegation step (delegate_to: {s['delegate_to']})"
                            if "delegate_to" in s
                            else "Synthetic — no hook invocations recorded"
                        )
                        step_gate = s.get("gate")
                        synthetic = {
                            "name": sname,
                            "index": si,
                            "status": mstep.get("status", "unknown"),
                            "config": {
                                "spec_check": s.get("spec_check", True),
                                "subagent": s.get("subagent", True),
                                "gate": step_gate,
                            },
                            "goal": s.get("goal"),
                            "inputs": s.get("inputs"),
                            "outputs": s.get("outputs"),
                            "started_at": mstep.get("started_at"),
                            "completed_at": mstep.get("completed_at"),
                            "duration_ms": mstep.get("duration_ms"),
                            "invocations": [],
                            "retry_count": 0,
                            "summary": mstep.get("summary"),
                            "tool_warnings": mstep.get("tool_warnings"),
                            "note": note,
                        }
                        trace["steps"].append(synthetic)

            # Merge tool_warnings and prompt_files from manifest into trace steps
            for trace_step in trace.get("steps", []):
                sname = trace_step.get("name")
                mstep = manifest.get("steps", {}).get(sname, {})
                tw = mstep.get("tool_warnings")
                if tw and "tool_warnings" not in trace_step:
                    trace_step["tool_warnings"] = tw
                pf = mstep.get("prompt_files")
                if pf:
                    # Embed prompt file content for trace viewer
                    enriched = []
                    for i, fp in enumerate(pf):
                        entry = {
                            "path": fp,
                            "branch_index": i,
                        }
                        try:
                            with open(fp, "r", encoding="utf-8") as pff:
                                entry["content"] = pff.read()
                        except (FileNotFoundError, OSError):
                            entry["content"] = None
                        enriched.append(entry)
                    trace_step["prompt_files"] = enriched

            # Sort steps by definition-order index
            trace["steps"].sort(key=lambda s: s.get("index", 999))

            # Embed MCP call log into trace
            # Include calls that match this workflow by name, plus
            # utility calls (resolve, list_agent_files) that also
            # reference this workflow (or have no workflow context).
            wf_calls = [
                c for c in _mcp_call_log
                if c.get("workflow") == workflow_name
                or (c.get("tool") in ("workflow_resolve", "list_agent_files")
                    and c.get("workflow") in (workflow_name, None))
            ]
            if wf_calls:
                trace["mcp_calls"] = wf_calls
                trace["total_mcp_calls"] = len(wf_calls)
            # Clear only this workflow's entries (keep other workflows' calls)
            consumed = set(id(c) for c in wf_calls)
            _mcp_call_log[:] = [
                c for c in _mcp_call_log if id(c) not in consumed
            ]

            # Check error log for warnings
            error_log = os.path.join(AGENT_DIR, "gate-check-error.log")
            if os.path.isfile(error_log):
                try:
                    with open(error_log, "r", encoding="utf-8") as f:
                        errors = f.read().strip()
                    if errors and run_id in errors:
                        trace["warnings"] = [
                            line
                            for line in errors.split("\n")
                            if run_id in line
                        ]
                except Exception:
                    pass

            with open(trace_path, "w", encoding="utf-8") as f:
                json.dump(trace, f, indent=2, ensure_ascii=False)
                f.write("\n")

            trace_finalized = True
        except Exception as e:
            _log(f"Trace finalization failed: {e}")

    # ── Merge tool-calls.json into trace ──
    tool_calls_path = os.path.join(runtime_dir, "tool-calls.json")
    tool_calls = []
    if os.path.isfile(tool_calls_path):
        try:
            with open(tool_calls_path, "r", encoding="utf-8") as f:
                tool_calls = json.load(f)
            _log(f"Merged {len(tool_calls)} tool calls into trace")
        except (json.JSONDecodeError, OSError) as e:
            _log(f"Warning: failed to read tool-calls.json: {e}")

    if tool_calls and os.path.isfile(trace_path):
        try:
            with open(trace_path, "r", encoding="utf-8") as f:
                trace_data = json.load(f)
            trace_data["tool_calls"] = tool_calls
            trace_data["total_tool_calls_proxy"] = len(tool_calls)
            with open(trace_path, "w", encoding="utf-8") as f:
                json.dump(trace_data, f, indent=2, ensure_ascii=False)
                f.write("\n")
        except Exception as e:
            _log(f"Warning: failed to merge tool calls into trace: {e}")

    # Clean up tool-calls.json (per-run, regenerated each workflow_init)
    if os.path.isfile(tool_calls_path):
        try:
            os.remove(tool_calls_path)
        except OSError:
            pass

    # Phase 38: no per-workflow proxy cleanup, no AGENT.md restore — neither
    # exists anymore. workflow-tools-loader.py is stable in .mcp.json and
    # per-step subagent files are managed by /init-workflow.

    # ── Build summary ──
    step_statuses = {
        sname: sdata.get("status", "unknown")
        for sname, sdata in manifest.get("steps", {}).items()
    }

    return {
        "workflow": workflow_name,
        "status": final_status,
        "steps": step_statuses,
        "trace_finalized": trace_finalized,
        "run_id": run_id,
    }


